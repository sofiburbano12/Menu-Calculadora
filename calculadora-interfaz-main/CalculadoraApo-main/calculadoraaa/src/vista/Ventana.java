    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // TODO add your handling code here:
        // boton igual
        float numero2;
        numero2 = (int) Float.parseFloat (pantalla.getText());
        switch (signo) {
            case "+":
           pantalla.setText(operaciones.Sumar(numero1, numero2));
            break;
            case "-":
             pantalla.setText(operaciones.Restar(numero1, numero2));
             break;
              case "*":
             pantalla.setText(operaciones.Multiplicacion(numero1, numero2));
             break;
              case "/": 
             pantalla.setText(operaciones.Division(numero1, numero2));
             break;
        }
    
       
    }//GEN-LAST:event_jButton13ActionPerformed

