    public String cero(float resultadoo) {
        
        String retorno = "";
        retorno=Float.toString(resultadoo);
        
        if(resultadoo %1==0) {
            retorno=retorno.substring(0, retorno.length()-2);
            
        }
        return retorno;
    }
    

